package MethodsExercises;

public class calculator {

	public static void sum(double n1, double n2) {
		double result = n1 + n2;
		System.out.println("The sum of " + n1 + " plus " + n2 + " is: " + result);
	}
	public static void subtraction(double n1, double n2) {
		double result = n1 - n2;
		System.out.println("Subtracting " + n1 + " minus " + n2 + " is: " + result);
	}
	public static void multiplication(double n1, double n2) {
		double result = n1 * n2;
		System.out.println("Multiplying " + n1 + " by " + n2 + " is: " + result);
	}
	public static void division(double n1, double n2) {
		double result = n1 / n2;
		System.out.println("The division of " + n1 + " by " + n2 + " is: " + result);
	}
}
